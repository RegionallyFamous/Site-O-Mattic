import fs from "node:fs/promises";
import path from "node:path";
import { readSpec, specTargets } from "./spec-utils.mjs";

const appCatalogPath = path.join("app", "blueprint-catalog.ts");
const apiRoot = path.join("app", "api", "blueprints");
const visualSweepRoot = path.join("qa", "reports", "visual-sweep");
const generatedNotice = "/* This file is generated by src/sync-app-blueprints.mjs. */";

const targets = await specTargets();
const specs = [];

for (const target of targets) {
  specs.push(await readSpec(target));
}

specs.sort((a, b) => a.slug.localeCompare(b.slug));

await fs.mkdir(apiRoot, { recursive: true });
await pruneStaleRoutes(new Set(specs.map((spec) => spec.slug)));
await writeCatalog(specs);

for (const spec of specs) {
  await writeRoute(spec);
}

console.log(`Synced ${specs.length} blueprint app routes and catalog entries.`);

async function writeCatalog(items) {
  const entries = [];

  for (const spec of items) {
    const manifestPath = path.join("public", "blueprints", spec.slug, "asset-manifest.json");
    const blueprintPath = path.join("public", "blueprints", spec.slug, "blueprint.json");
    const manifest = JSON.parse(await fs.readFile(manifestPath, "utf8"));
    await fs.access(blueprintPath);

    const hero = `/blueprints/${spec.slug}${manifest.assets.hero.outputPath}`;
    const screenshot = await screenshotFor(spec.slug, hero);

    entries.push({
      slug: spec.slug,
      name: spec.businessName,
      eyebrow: titleCase(spec.niche),
      summary: summarize(spec.copy.heroText),
      logo: `/blueprints/${spec.slug}${manifest.assets.logo.outputPath}`,
      logoWidth: manifest.assets.logo.width,
      logoHeight: manifest.assets.logo.height,
      hero,
      heroWidth: manifest.assets.hero.width,
      heroHeight: manifest.assets.hero.height,
      screenshot,
      alt: spec.assetMeta.hero.alt,
      path: `/api/blueprints/${spec.slug}/blueprint.json`,
      layoutVariant: spec.layoutVariant,
      releaseStatus: spec.release.status
    });
  }

  const source = `${generatedNotice}

export const blueprints = ${JSON.stringify(entries, null, 2)} as const;
`;

  await fs.writeFile(appCatalogPath, source);
}

async function screenshotFor(slug, fallback) {
  const source = path.join(visualSweepRoot, slug, "desktop.png");
  const targetPath = "/assets/screenshot.png";
  const target = path.join("public", "blueprints", slug, targetPath);

  try {
    await fs.access(source);
  } catch {
    return fallback;
  }

  await fs.mkdir(path.dirname(target), { recursive: true });
  await fs.copyFile(source, target);

  return `/blueprints/${slug}${targetPath}`;
}

async function writeRoute(spec) {
  const routeDir = path.join(apiRoot, spec.slug, "blueprint.json");
  const routePath = path.join(routeDir, "route.ts");
  await fs.mkdir(routeDir, { recursive: true });

  const source = `${generatedNotice}

import blueprint from "@/public/blueprints/${spec.slug}/blueprint.json";

const responseHeaders = {
  "access-control-allow-origin": "*",
  "access-control-allow-methods": "GET, OPTIONS",
  "access-control-allow-headers": "content-type",
  "cache-control": "public, max-age=300",
  "content-type": "application/json; charset=utf-8",
};

export function GET() {
  return new Response(JSON.stringify(blueprint), {
    headers: responseHeaders,
  });
}

export function OPTIONS() {
  return new Response(null, {
    status: 204,
    headers: responseHeaders,
  });
}
`;

  await fs.writeFile(routePath, source);
}

async function pruneStaleRoutes(expectedSlugs) {
  const entries = await fs.readdir(apiRoot, { withFileTypes: true }).catch(() => []);

  for (const entry of entries) {
    if (!entry.isDirectory() || expectedSlugs.has(entry.name)) {
      continue;
    }

    const routePath = path.join(apiRoot, entry.name, "blueprint.json", "route.ts");
    const routeSource = await fs.readFile(routePath, "utf8").catch(() => "");
    if (routeSource.includes(generatedNotice)) {
      await fs.rm(path.join(apiRoot, entry.name), { recursive: true, force: true });
    }
  }
}

function summarize(value) {
  const text = String(value || "").replace(/\s+/g, " ").trim();
  if (text.length <= 160) {
    return text;
  }
  return `${text.slice(0, 157).replace(/\s+\S*$/, "")}...`;
}

function titleCase(value) {
  return String(value || "")
    .split(/\s+/)
    .filter(Boolean)
    .map((word) => `${word.slice(0, 1).toUpperCase()}${word.slice(1)}`)
    .join(" ");
}
