import fs from "node:fs/promises";
import path from "node:path";
import { buildDesignTokens } from "./blueprint-style-system.mjs";
import { layoutArchetypeFor, layoutVariantFor, renderFamilyForVariant } from "./layout-archetypes.mjs";
import {
  richCoreSignature,
  silhouetteContract,
  styleFamilyContract,
  validatePatternMatrixFit
} from "./production-polish-matrix.mjs";
import { readSpec, specTargets } from "./spec-utils.mjs";

const OUTPUT_PATH = path.join("docs", "production-redo-manifest.md");
const REQUIRED_PATTERN_FIELDS = [
  "primaryPattern",
  "secondaryPattern",
  "silhouette",
  "navigationPrimitive",
  "mobileActionPattern",
  "imageEvidence",
  "ctaRhythm",
  "surfaceFamily",
  "styleFamily",
  "density",
  "colorRoles",
  "geometry",
  "coreBlockPlan",
  "knownRisks",
  "styleContract"
];
const writeMode = process.argv.includes("--write");
const specPaths = await specTargets(process.argv.slice(2).filter((arg) => arg !== "--write"));
const specs = [];
const failures = [];

for (const specPath of specPaths) {
  const spec = await readSpec(specPath);
  specs.push({ spec, specPath });
  validateSpec(spec, specPath, failures);
}

const manifest = buildManifest(specs);
const output = `${manifest}\n`;

if (writeMode) {
  await fs.mkdir(path.dirname(OUTPUT_PATH), { recursive: true });
  await fs.writeFile(OUTPUT_PATH, output);
  console.log(`Wrote ${OUTPUT_PATH}`);
} else {
  let current = "";
  try {
    current = await fs.readFile(OUTPUT_PATH, "utf8");
  } catch {
    failures.push(`${OUTPUT_PATH} is missing. Run npm run blueprint:manifest:write.`);
  }
  if (current && current !== output) {
    failures.push(`${OUTPUT_PATH} is stale. Run npm run blueprint:manifest:write.`);
  }
}

validateDistribution(specs, failures);

if (failures.length) {
  console.error("Production redo manifest failed:");
  for (const failure of failures) {
    console.error(`- ${failure}`);
  }
  process.exit(1);
}

console.log(`Production redo manifest OK: ${specs.length} specs.`);

function buildManifest(items) {
  const rows = items
    .map(({ spec, specPath }) => buildRow(spec, specPath))
    .sort((left, right) => left.slug.localeCompare(right.slug));

  const distribution = buildDistribution(rows);
  const table = [
    "| Niche | Slug | Pattern | Silhouette | Nav | Mobile Action | Render Family | Typography | Actual Type | Style Voice | Type Category | Color Behavior | Image Evidence | CTA | Core Block Feature | Risk |",
    "| --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- |",
    ...rows.map((row) => [
      row.niche,
      code(row.slug),
      row.pattern,
      row.silhouette,
      row.nav,
      row.mobileAction,
      row.renderFamily,
      row.typography,
      row.actualType,
      row.styleVoice,
      row.typeCategory,
      row.colorBehavior,
      row.imageEvidence,
      row.cta,
      row.coreBlockFeature,
      row.risk
    ].map(mdCell).join(" | ")).map((line) => `| ${line} |`)
  ].join("\n");

  return `# Production Redo Manifest

Generated by \`npm run blueprint:manifest:write\`. This is the pre-flight map for full Site-O-Mattic rebuilds: every approved niche should have a conversion pattern, visible silhouette, navigation primitive, mobile action behavior, distinct type category, role-based color behavior, image evidence, CTA rhythm, at least one structural core-block feature, and known QA risks.

## Batch Summary

- Specs: ${rows.length}
- Layout variants: ${distribution.layoutVariants}
- Render families: ${distribution.renderFamilies}
- Navigation primitives: ${distribution.navigationPrimitives}
- Style families: ${distribution.styleFamilies}
- Type categories: ${distribution.typeCategories}
- Display font families: ${distribution.displayFontFamilies}
- Accent font families: ${distribution.accentFontFamilies}
- Rich core-block features: ${distribution.coreBlockFeatures}
- Silhouettes: ${distribution.silhouettes}

## Redo Map

${table}
`;
}

function buildRow(spec, specPath) {
  const archetype = layoutArchetypeFor(spec);
  const pattern = spec.pattern || {};
  const coreBlockFeature = richCoreSignature(pattern.coreBlockPlan || []);
  const typography = archetype.typographyTreatment || "unknown";
  const type = buildDesignTokens(spec).typography.custom;
  const style = styleFamilyContract(pattern.styleFamily);
  const silhouette = silhouetteContract(pattern.silhouette);
  return {
    niche: spec.niche || spec.businessName || spec.slug,
    slug: spec.slug,
    specPath,
    pattern: pattern.primaryPattern,
    silhouette: pattern.silhouette,
    nav: pattern.navigationPrimitive,
    mobileAction: pattern.mobileActionPattern,
    renderFamily: renderFamilyForVariant(layoutVariantFor(spec)),
    typography,
    displayFontFamily: primaryFontFamily(type.displayFont),
    accentFontFamily: primaryFontFamily(type.accentFont),
    actualType: `${primaryFontFamily(type.displayFont)} / ${primaryFontFamily(type.accentFont)}`,
    styleVoice: style?.typeVoice || silhouette?.signatureMove || pattern.styleFamily,
    styleFamily: pattern.styleFamily,
    typeCategory: typographyCategory(spec, archetype),
    colorBehavior: colorBehavior(spec, archetype),
    imageEvidence: pattern.imageEvidence,
    cta: pattern.ctaRhythm,
    coreBlockFeature,
    risk: Array.isArray(pattern.knownRisks) ? pattern.knownRisks.slice(0, 2).join(", ") : "",
    layoutVariant: layoutVariantFor(spec)
  };
}

function validateSpec(spec, specPath, targetFailures) {
  if (!spec.slug) {
    targetFailures.push(`${specPath} is missing slug.`);
  }
  if (!spec.pattern || typeof spec.pattern !== "object") {
    targetFailures.push(`${specPath} is missing pattern.`);
    return;
  }
  for (const field of REQUIRED_PATTERN_FIELDS) {
    if (spec.pattern[field] === undefined || spec.pattern[field] === null || spec.pattern[field] === "") {
      targetFailures.push(`${specPath} missing pattern.${field}.`);
    }
  }
  if (!Array.isArray(spec.pattern.coreBlockPlan) || !spec.pattern.coreBlockPlan.length) {
    targetFailures.push(`${specPath} needs pattern.coreBlockPlan entries.`);
  }
  if (!Array.isArray(spec.pattern.knownRisks) || !spec.pattern.knownRisks.length) {
    targetFailures.push(`${specPath} needs pattern.knownRisks entries.`);
  }
  for (const error of validatePatternMatrixFit(spec.pattern)) {
    targetFailures.push(`${specPath} ${error}`);
  }
}

function validateDistribution(items, targetFailures) {
  const rows = items.map(({ spec, specPath }) => buildRow(spec, specPath));
  const richFeatures = unique(rows.map((row) => row.coreBlockFeature).filter((value) => value !== "group/columns/buttons"));
  const typeCategories = unique(rows.map((row) => row.typeCategory));
  const navigationPrimitives = unique(rows.map((row) => row.nav));
  const silhouettes = unique(rows.map((row) => row.silhouette));
  const styleFamilies = unique(rows.map(({ styleFamily }) => styleFamily));
  const renderFamilies = unique(rows.map((row) => row.renderFamily));
  const displayFontFamilies = unique(rows.map((row) => row.displayFontFamily));
  const accentFontFamilies = unique(rows.map((row) => row.accentFontFamily));

  if (rows.length < 35) {
    targetFailures.push(`Expected at least 35 production specs before full redo, found ${rows.length}.`);
  }
  if (renderFamilies.length < 10) {
    targetFailures.push(`Render-family distribution is too narrow: ${renderFamilies.join(", ") || "none"}.`);
  }
  if (displayFontFamilies.length < 7) {
    targetFailures.push(`Display font distribution is too narrow: ${displayFontFamilies.join(", ") || "none"}.`);
  }
  if (accentFontFamilies.length < 6) {
    targetFailures.push(`Accent font distribution is too narrow: ${accentFontFamilies.join(", ") || "none"}.`);
  }
  if (typeCategories.length < 4) {
    targetFailures.push(`Typography distribution is too narrow: ${typeCategories.join(", ") || "none"}.`);
  }
  if (richFeatures.length < 6) {
    targetFailures.push(`Core block distribution is too narrow: ${richFeatures.join(", ") || "none"}.`);
  }
  if (navigationPrimitives.length < 5) {
    targetFailures.push(`Navigation primitive distribution is too narrow: ${navigationPrimitives.join(", ") || "none"}.`);
  }
  if (silhouettes.length < 8) {
    targetFailures.push(`Silhouette distribution is too narrow: ${silhouettes.join(", ") || "none"}.`);
  }
  if (styleFamilies.length < 6) {
    targetFailures.push(`Style family distribution is too narrow: ${styleFamilies.join(", ") || "none"}.`);
  }
}

function buildDistribution(rows) {
  return {
    layoutVariants: unique(rows.map((row) => row.layoutVariant)).length,
    renderFamilies: unique(rows.map((row) => row.renderFamily)).length,
    navigationPrimitives: unique(rows.map((row) => row.nav)).length,
    styleFamilies: unique(rows.map((row) => row.styleFamily)).length,
    typeCategories: unique(rows.map((row) => row.typeCategory)).length,
    displayFontFamilies: unique(rows.map((row) => row.displayFontFamily)).length,
    accentFontFamilies: unique(rows.map((row) => row.accentFontFamily)).length,
    coreBlockFeatures: unique(rows.map((row) => row.coreBlockFeature)).length,
    silhouettes: unique(rows.map((row) => row.silhouette)).length
  };
}

function typographyCategory(spec, archetype) {
  const haystack = [
    spec.layoutVariant,
    spec.pattern?.styleFamily,
    spec.pattern?.surfaceFamily,
    archetype.typographyTreatment,
    archetype.colorStrategy
  ].filter(Boolean).join(" ").toLowerCase();

  if (/mono|receipt|ledger|ticket|workbench|console/.test(haystack)) {
    return "utility-mono-ledger";
  }
  if (/condensed|industrial|urgent|risk|route|status|dashboard|operator/.test(haystack)) {
    return "condensed-utility";
  }
  if (/serif|editorial|gallery|portrait|floral|wedding|dessert|mural|fashion|proof-stage/.test(haystack)) {
    return "editorial-display";
  }
  if (/geometric|mobile|gloss|package|menu|event|hospitality|booth|cart/.test(haystack)) {
    return "geometric-commercial";
  }
  if (/humanist|calm|care|domestic|consult|story|local/.test(haystack)) {
    return "humanist-service";
  }
  return "system-utility";
}

function colorBehavior(spec, archetype) {
  const roles = spec.pattern?.colorRoles || {};
  const action = roles.action ? `action:${roles.action}` : "";
  const proof = roles.proof ? `proof:${roles.proof}` : "";
  const strategy = archetype.colorStrategy || "role-based";
  return [strategy, action, proof].filter(Boolean).join("; ");
}

function unique(values) {
  return [...new Set(values.filter(Boolean))].sort();
}

function mdCell(value) {
  return escapeMarkdown(String(value ?? ""));
}

function code(value) {
  return `\`${value}\``;
}

function primaryFontFamily(stack) {
  return String(stack || "unknown")
    .split(",")[0]
    .replace(/^["']|["']$/g, "")
    .trim() || "unknown";
}

function escapeMarkdown(value) {
  return value
    .replaceAll("|", "\\|")
    .replaceAll("\n", " ")
    .trim();
}
